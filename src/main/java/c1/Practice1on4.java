package c1;

public class Practice1on4 {

  public static void main(String[] args) {
    int a=29;
    int b=13;

    System.out.println("a & b:"+(a&b));
    System.out.println("a | b:"+(a|b));
    System.out.println("a ^ b:"+(a^b));
    System.out.println("~a:"+(~a));

    System.out.println("a << 2 :"+(a<<2));
    System.out.println("a >> 2 :"+(a>>2));
    System.out.println("a >>> 2 :"+(a>>>2));
  }

}
